let d = new Date(); // get the date
console.log("Date is " + d);
console.log("Date is " + d.getDate());
console.log("Month is " + d.getMonth() + 1); // month range 0-11, so add +1 to it
console.log("Year is " + d.getFullYear()); // year
console.log("Milli seconds since 1970 jan is " + d.getTime()); // milli seconds since jan 1970
console.log("Seconds is " + d.getSeconds()); // seconds
console.log("Minutes is " + d.getMinutes()); // Minutes
console.log("Hours is " + d.getHours()); // Hours 0-24 hr
